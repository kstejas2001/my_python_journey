# Tiny examples
fruits = ["apple", "banana", "mango"]
print(fruits[0]) # apple

fruits.append("orange")
print(fruits)
fruits.insert(1, "kiwi")
print(f"Inserting new item at the 2nd position: {fruits}")

print(f"List after slice: {fruits[1:4]}")

print("mango" in fruits)

